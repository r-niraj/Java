package com.company;

public class ResultingDataType_AfterArithmaticOperation {
    public static void main(String[] args) {
        int i = 23;
        short s = 2;
        float f = 3.23f;
        double d = 3.45;
        byte b = 1;
        char c = 'A';
        System.out.println("i+s="+(i+s));
        System.out.println("i+f="+(i+f));
        System.out.println("b+s="+(b+s));
        System.out.println("i+c="+(i+c));
        System.out.println("f+d="+(f+d));
    }
}
