package com.company;

public class JavaLiterals {
    public static void main(String[] args){
        byte age  = 34;
        int age2 = 58;
        long num1 = 54321678978l; //here 'l/L' is to declare that the number is long
        char ch = 'A';
        float f1 = 5.1f; //to declare that "5.1" is a float number
        double d1 = 4.77d; //we can also write "4.77" as floating points are double in default
        boolean a = true;
        System.out.println(age);
        String str = "Shawon";
        System.out.println(str);
    }
}
